    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        boolean resultado;
        this.num=0;
        
        try {
            num = Integer.parseInt(this.tamLista.getText());
            if(num < 0)
            {
                num=0;
            }
            if(num>200)
            {
                num=100;
            }
            resultado = true;
        } catch (NumberFormatException excepcion) {
            resultado = false;
        }
        this.lista.setText("");
        if(resultado==true)
        {
            BalanceCargas bc = new BalanceCargas("lista");
            bc.generarLista(num);
           this.listaO = bc.getLista();
            System.out.println("---------Lista Generada---------\n");
            for (Peticion p: bc.getLista())
            {
                this.lista.setText(""+this.lista.getText()+"\n         "+p.getIdentificador()+"           "+p.getTiempo());
                System.out.println(p.toString());
            }
            
            tam1.setEnabled(true);
            tam2.setEnabled(true);
            tam3.setEnabled(true);
            aceptar.setEnabled(true);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        // TODO add your handling code here:
        boolean resultado;
        
        int num1 = 0;
        int num2 = 0;
        int num3 = 0;
        int total=0;
        int y=0;
        
        try {
                num1 = Integer.parseInt(this.tam1.getText());
                num2 = Integer.parseInt(this.tam2.getText());
                num3 = Integer.parseInt(this.tam3.getText());
                total = num1+num2+num3;
            if(num1<0)
            {
                num1=0;
            }
            if(num2<0)
            {
                num2=0;
            }
            if(num3<0)
            {
                num3=0;
            }
            if(total>this.listaO.size())
            {
                if(num1> num2 && num1 > num3)
                {
                    num1 = this.listaO.size();
                    num2 = 0;
                    num3 = 0;
                }else
                {
                    if(num2 > num1 && num2 > num3)
                    {
                        num1 = 0;
                        num2 = this.listaO.size();
                        num3 = 0;
                    }else
                    {
                        num1 = 0;
                        num2 = 0;
                        num3 = this.listaO.size();
                    }
                }
            }

            resultado = true;
        } catch (NumberFormatException excepcion) {
            resultado = false;
        }
        
        
        if(resultado == true)
        {
            ArrayList<Peticion> nodo1 = new ArrayList<>();
            ArrayList<Peticion> nodo2 = new ArrayList<>();
            ArrayList<Peticion> nodo3 = new ArrayList<>();

            if(num1>0)
            {
                for(int x = 0 ; x < num1 ; x++)
                {
                    if(this.listaO.size()>0 && y<num1 )
                    {
                        nodo1.add(this.listaO.get(x));
                        this.listaO.remove(x);
                        x=-1;
                        y++;
                    }
                }
            }
            y=0;
            if(num2>0)
            {
                for(int x = 0 ; x < num2 ; x++)
                {
                    if(this.listaO.size()>0 && y<num2)
                    {
                        nodo2.add(this.listaO.get(x));
                        this.listaO.remove(x);
                        x=-1;
                        y++;
                    }
                }
            }
            y=0;
            if(num3>0)
            {
                for(int x = 0 ; x < num3 ; x++)
                {
                    if(this.listaO.size()>0 && y<num3)
                    {
                        nodo3.add(this.listaO.get(x));
                        this.listaO.remove(x);
                        x=-1;
                        y++;
                    }
                }
            }

            double initialTime = System.currentTimeMillis();

            BalanceCargas bc = new BalanceCargas("Nodo1",num1,nodo1,initialTime,this.listaO);
            BalanceCargas bc1 = new BalanceCargas("Nodo2",num2,nodo2,initialTime,this.listaO);
            BalanceCargas bc2 = new BalanceCargas("Nodo3",num3,nodo3,initialTime,this.listaO);

            bc.start();
            bc1.start();
            bc2.start();
            
            tam1.setEnabled(false);
            tam2.setEnabled(false);
            tam3.setEnabled(false);
            aceptar.setEnabled(false);
        }

    }//GEN-LAST:event_aceptarActionPerformed

